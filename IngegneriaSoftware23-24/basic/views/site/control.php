 <br><br><br>
    <p>Fornire agli interessati un controllo adeguato sul trattamento
dei loro dati personali.
Il controllo è un principio fondamentale per tutelare la privacy degli utenti.
Lo scopo principale della privacy non è impedirne totalmente il trattamento e la condivisione
dei dati personali. Affatto! Ma gli utenti vogliono avere il controllo e avere a
dire in che modo i loro dati personali vengono elaborati e condivisi.

</p>

<br>
<br>
<br>
<h1 style="font-family:Impact;">Strategie</h1>
<br>
<p>La minimizzazione dei dati personali può essere ottenuta raccogliendo dati da
meno persone o raccogliendo meno dati sulle persone. Esistono diverse tattiche
applicabile.</p>
<br>
<h2 style="text-align:left;font-family:Impact;">-Scegliere</h2>
<br>
<p>Offri agli utenti una scelta reale: le funzionalità di base dovrebbero essere accessibili
per le persone che non acconsentono al trattamento dei propri dati personali
. Offri un'alternativa (a pagamento).</p>
<br>

<h2 style="text-align:left;font-family:Impact;">-Consentire</h2>
<br>
<p>Chiedere agli utenti il ​​consenso esplicito al trattamento dei propri dati personali
. Informarli in anticipo esattamente su quali dati personali
verranno trattati, come verranno trattati e per quali finalità. Dovrebbe
essere possibile revocare il consenso.</p>
<br>
<h2 style="text-align:left;font-family:Impact;">-Aggiornare</h2>
<br>
<p>Offrire agli utenti i mezzi per rivedere e aggiornare i dati personali
raccolti su di loro. Un approccio logico è combinare questo con
l'approccio che consente agli utenti di visualizzare i dati personali raccolti
(ad esempio attraverso una dashboard).
</p>
<br>
<h2 style="text-align:left;font-family:Impact;">-Cancellare</h2>
<br>
<p>Offrire agli utenti un mezzo per ritirare (o chiedere la cancellazione) dei propri file
 personali. Ancora una volta, questo può far parte di un dashboard sulla privacy.</p>
<br>
<br>
<br>
<br>
<h5>Il consenso non è sempre richiesto quando si trattano dati personali, ad esempio
quando hai un interesse legittimo a trattarli. Consultare sempre un'
avvocato per essere sicuri.
Non è sempre possibile o addirittura necessario consentire agli utenti di correggere i propri
dati o di cancellare i loro dati quando te lo chiedono. A volte 
i dati personali devono semplicemente essere conservati. Nelle cartelle cliniche non è auspicabile
di consentire ai pazienti di modificare voci con significato medico.
</h5>
<br>
<br>
<br>

           

<h1 style="font-family:Impact;">Esempi</h1>

<br>
<br>
<p>In molti casi, ad esempio, il trattamento dei dati personali è semplicemente consentito
perché strettamente necessari per dare esecuzione ad un contratto (es
l'indirizzo è richiesto quando si ordina qualcosa on-line) o perché 
è un requisito legale per farlo (ad esempio le banche verificano la tua identità). In
 altri casi è necessario il consenso. Informare chiaramente gli utenti sugli scopi.
E offri loro una scelta reale (in modo che abbiano accesso anche quando non lo fanno,
facendo accedere ad una parte limitata del servizio). Utilizza l'attivazione  anziché opt-out (il trattamento richiede
luoghi, salvo revoca del consenso in una fase successiva): la scelta predefinita
non costituisce consenso. Quindi non utilizzare caselle di controllo preselezionate per
iscrivere le persone alle newsletter, ad esempio...</p>

            <br>

<p>I siti web devono chiedere il permesso per inserire cookie. Molti delle notifiche  "accetta i cookie".
 non sono conformi: non offrono una scelta reale in quanto
il sito web non può essere visitato se non si accetta il cookie. Una vera richiesta di cookie 
 offre la scelta reale di accettare i cookie e offre la possibilità di fare
scegliere quali cookie accettare o meno (es. quelli necessari per i cookie anonimi
statistiche del sito web o quelle utilizzate per connettersi ai social network).</p>
<br>
<p>
Un approccio radicalmente diverso sposta il controllo dei dati personali
completamente agli utenti stessi. Invece di organizzazioni che memorizzano i
dati personali per tutti i loro clienti, chiedono ai loro clienti in ciascun negozio
quei dati stessi. Le organizzazioni accedono successivamente a tali informazioni
attraverso un'interfaccia standard, sotto il controllo dell'utente. Questo è a volte 
 chiamato “relazioni gestite dal cliente” in contrapposizione a “management relazioni con il cliente”.»
</p>
