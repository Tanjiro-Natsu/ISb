 <br><br><br>
    <p>Impegnarsi a trattare i dati personali nel rispetto della privacy in 
modo da applicarlo adeguatamente.
La privacy non dovrebbe essere garantita solo attraverso mezzi tecnici, ma anche
attraverso mezzi organizzativi. Dovrebbe far parte dell'organizzazione
culturale ed essere propagato dal management superiore. Altrimenti nessuno
si sentirà responsabile. Una chiara politica sulla privacy fornirà scope e gaudience. La strategia di applicazione è orientata internamente, verso l’organizzazione
 stessa. La strategia garantisce che la privacy venga comunicata all'esterno
La dichiarazione  viene applicata anche internamente attraverso una
politica sulla riservatezza.</p>

<br>
<br>
<br>
<h1 style="font-family:Impact;">Strategie</h1>
<br>
<p>L'elaborazione rispettosa della privacy viene applicata applicando le seguenti tattiche.</p>
<br>
<h2 style="text-align:left;font-family:Impact;">-Creare</h2>
<br>
<p>L’organizzazione dovrebbe impegnarsi per la privacy. Assumersi la responsabilità.
Creare una politica sulla privacy. Assegnare risorse per eseguire questa politica.
Determinare per ciascun processo l'obiettivo e la base (legale): c'è
un interesse legittimo o è necessario il consenso? Sii chiaro riguardo al
modello di business.</p>
<br>

<h2 style="text-align:left;font-family:Impact;">-Mantenere</h2>
<br>
<p>Mantenere la politica con tutte le necessarie misure tecniche ,organizzative e
controlli nazionali. Implementare questi controlli. Assegnare le responsabilità. Creare una campagna di sensibilizzazione per formare tutto il personale. Fare
in modo che anche alcuni  third parties (processors) rispettano la politica.</p>
<br>
<h2 style="text-align:left;font-family:Impact;">-Sostenere</h2>
<br>
<p>Le circostanze cambiano. Verificare regolarmente la politica sulla privacy e
adeguare la sua attuazione ogniqualvolta sia necessario. Stabilire criteri precedenti
 e valuta la tua politica nei loro confronti.

</p>
<br>
<br>
<br>
<br>
<h5>La politica sulla privacy dovrebbe essere in linea con il piano aziendale generale e
dichiarazione di intenti dell’organizzazione. Assicurati che sia coerente con tutte le
altre politiche organizzative.
</h5>
<br>
<br>
<br>

            <br>
<h1 style="font-family:Impact;">Esempi</h1>

<br>
<br>
<p>Un approccio consiste nell'implementare un sistema di gestione della privacy simile a
il ciclo plan-do-check-act del <a style="color:green;" href="https://www.iso.org/standard/27001">information security management
standard (ISO 27001).</a>Ciò potrebbe essere integrato con le 
valutazioni di impatto sulla protezione dei dati che devono comunque essere eseguite.
</p>
<br>
<p>Un altro approccio tecnico è l’uso delle cosiddette “politiche appiccicose”.
viene allegato un dato per indicare lo scopo originario per il quale il dato 
è stato raccolto  o per specificare il trattamento consentito da 
eseguire sul dato. I processi nell'organizzazione sono impostati in
in modo tale che per ciascun dato sia presente la politica fissa ad esso associata
verificato automaticamente.</p>
<br>
<p>Si potrebbe anche considerare l’uso di tecniche che registrano azioni “insolite”
 e che bloccano, ad esempio, l'accesso ai dati quando necessario,
quando una persona tenta di accedere a una quantità insolita di dati o se
poche persone accedono a un particolare dato (ad esempio i dati medici
record di un VIP).</p>
<br>
<p>Dovrebbero essere presi in considerazione anche i processi di sviluppo del sistema
all'interno della tua organizzazione. Aderire alla privacy secondo la filosofia del design,
e affrontare la privacy fin dall'inizio.
Se non sviluppi i tuoi sistemi, privacy attraverso il design thinking e
in particolare l'use delle privacy strategies aiuta a settare 
i requisiti dei prodotti o servizi da acquistare.</p>
