<h3 style="text-align:left;font-family:Impact;margin:15px;">Esempi</h3>
<br>
<h6 style="text-align:left;font-family:Impact;margin:15px;"> Esempio 1</h5>
<br>
<p style="text-align:left;margin:15px;">Gmail visualizza informazioni su altre sessioni (se presenti) nel piè di pagina, collegandosi a una pagina denominata "Attività su questo account" che elenca altre sessioni e attività recenti sull'account Gmail. L'utente ha la possibilità di disconnettersi da altre sessioni. In caso di fastidiosi falsi positivi, l'utente può scegliere di disattivare l'avviso per attività insolite. La disattivazione richiede circa una settimana, "per essere sicuri che non siano i cattivi a disattivare i tuoi avvisi".</p>

<br><h6 style="text-align:left;font-family:Impact;margin:15px;"> Esempio 2</h5>
<br>
<p style="text-align:left;margin:15px;">Quando Facebook rileva un accesso insolito, mostra l'"autenticazione sociale" che mostra alcune foto degli amici dell'utente e chiede all'utente di nominare la persona in quelle foto.</p>
<br>
<h6 style="text-align:left;font-family:Impact;margin:15px;"> Esempio 3</h5>
<br>
<p style="text-align:left;margin:15px;">La scheda "Sicurezza" delle "Impostazioni" del sito Web Dropbox visualizza tutte le sessioni del browser Web registrate nell'account e consente all'utente di disconnettersi da una o più di esse. Per aiutare l'utente nella scelta vengono visualizzati il ​​nome del browser, il sistema operativo, l'indirizzo IP e il paese corrispondente. Visualizza inoltre tutti i dispositivi collegati all'account e consente all'utente di scollegarne uno o più.</p>

