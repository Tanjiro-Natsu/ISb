<h3 style="text-align:left;font-family:Impact;margin:15px;">Contesto</h3>
<br>
<p style="text-align:left;margin:15px;"> servizi (o prodotti), soprattutto su Internet, tendono a utilizzare l'autenticazione basata su nome utente e password. Questo meccanismo di sicurezza è molto conveniente per gli utenti, poiché è diffuso e semplice rispetto ad alternative più sicure. Tuttavia, è soggetto a difetti comuni. Le password diventano meno sicure poiché rimangono invariate, sono spesso vulnerabili ad attacchi di forza bruta, spionaggio e phishing e non è possibile dimostrare che siano detenute esclusivamente dall'utente. Ciò complica la certezza dell’autenticazione, e quindi l’autenticità di qualsiasi decisione presa dall’utente, compreso il consenso. I titolari del trattamento possono ricavare fattori aggiuntivi, come informazioni specifiche sul dispositivo o sull'accesso. Se viene fornita la posizione, ad esempio, potrebbe suggerire un'attività improbabile dell'account.</p>
