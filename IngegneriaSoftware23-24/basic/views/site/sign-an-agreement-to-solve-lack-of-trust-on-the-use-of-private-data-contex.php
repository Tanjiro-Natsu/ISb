<h3 style="text-align:left;font-family:Impact;margin:15px;">Contesto</h3>
<br>
<p style="text-align:left;margin:15px;">Gli utenti non si fidano intrinsecamente dei titolari del trattamento che forniscono servizi (o prodotti), poiché non hanno garanzie su quali siano le vere intenzioni del titolare del trattamento o del responsabile del trattamento. I titolari e i responsabili del trattamento in genere mirano a realizzare profitti, ma ciò potrebbe andare a discapito degli utenti se questi non considerano le loro esigenze di privacy. Il titolare del trattamento potrebbe avere valori predefiniti o livelli di controllo ragionevoli, ma anche gli utenti devono sentirsi rassicurati sul fatto che le loro scelte vengano rispettate. Ciò è particolarmente vero per ciò per cui forniscono o non forniscono il consenso legale.</p>
