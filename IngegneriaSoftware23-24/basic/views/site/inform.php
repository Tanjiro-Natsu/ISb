    <p>Informare gli interessati sul trattamento dei propri dati personali
 in modo tempestivo e adeguato.
Trasparenza su quali dati personali vengono trattati, come sono
trattati e per quale scopo, è un presupposto essenziale per una migliore
Protezione della privacy. Consente agli utenti di prendere decisioni  sull'utilizzo
di un sistema e acconsentendo al trattamento dei propri dati personali (vedi anche
la strategia di controllo). Inoltre consente alla società nel suo insieme di verificare se
le organizzazioni trattano i nostri dati personali in modo responsabile. (“La luce solare 
si dice che sia il migliore dei disinfettanti.”)</p>

<br>
<br>
<br>
<h1 style="font-family:Impact;">Strategie</h1>
<br>
<p>La minimizzazione dei dati personali può essere ottenuta raccogliendo dati da
meno persone o raccogliendo meno dati sulle persone. Esistono diverse tattiche
applicabile.</p>
<br>
<h2 style="text-align:left;font-family:Impact;">-Spiegare</h2>
<p>Spiega quali dati personali tratti e perché. Discuti perché
questo è necessario. Fallo in un modo chiaro e facile da capire, anche per un laico. Indirizza queste informazioni a un utente diverso: principianti, esperti, autorità. Prendi in considerazione l'utilizzo di un'approccio a strati
: fornire innanzitutto una panoramica e fornire collegamenti a informazioni più dettagliate in coda.</p>
<br>

<h2 style="text-align:left;font-family:Impact;">-Notificare</h2>
<p>Avvisa gli utenti (in tempo reale) nel momento in cui elabori i loro dati personali
, condividi con terzi,oppure non appena  verrai a conoscenza di
una fuga di dati. Preparare procedure chiare a questo scopo. Effettua notifiche
breve ma informative. Assicurati di non avvisare troppo spesso. Consenti agli  utenti
di controllare per quali eventi desiderano ricevere una notifica.
Informare gli utenti sul trattamento dei loro dati personali (attraverso un'
informativa sulla privacy) presuppone che esista una politica sulla privacy
su cui si basa il trattamento. Inoltre,
tali informazioni devono essere complete ed aggiornate.</p>
<br>
<h2 style="text-align:left;font-family:Impact;">-Fornire</h2>
<p>Fornire informazioni su quali dati personali vengono trattati, come
vengono elaborati e perché. Specificare chiaramente per quanto tempo 
i dati personali vengono conservati e come vengono eliminati. Elenca tutte le terze parti con
cui condividi questi dati personali, sii chiaro riguardo alle condizioni
che coprono ogni scambio di dati con terzi e ne specificano le modalità e quali condizioni 
vengono applicate. Inserisci un collegamento alla tua politica sulla privacy sul tua
home page e nella tua app. Indicare chiaramente come le persone possono entrare in 
contatto con domande sulla loro privacy</p>
