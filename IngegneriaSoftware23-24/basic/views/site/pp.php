<h2 style="font-family:Impact;color:white;">Informed Secure Passwords</h2>

<p style="font-family:Impact;"> Le credenziali sono richieste da numerosi servizi (e prodotti) per garantire che solo gli utenti autenticati e autorizzati abbiano accesso a determinate funzionalità. I controllori in genere forniscono meccanismi di autenticazione sotto forma di nomi utente e password. Sebbene forniscano una forma di sicurezza debole se utilizzati in modo errato, sono più convenienti per gli utenti rispetto a molte alternative meno popolari e più sicure. I controllori spesso cercano di aggirare le carenze delle password incoraggiando gli utenti a cambiarle frequentemente, a utilizzare varianti più forti, a controllarle e a impedirne la divulgazione e il riutilizzo. Tuttavia gli utenti utilizzano molti servizi e utilizzano molte password, scoraggiando così una corretta applicazione. Questa applicazione errata può comportare l'accesso ai dati personali da parte di persone non autorizzate.</p>
