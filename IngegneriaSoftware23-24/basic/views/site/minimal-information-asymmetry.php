<h3 style="text-align:left;font-family:Impact;margin:15px;">Contesto</h3>
<br>
<p style="text-align:left;margin:15px;">Gli utenti interagiscono spesso con titolari del trattamento di cui non hanno mai utilizzato i servizi (o prodotti) in precedenza. A questo punto, la conoscenza che l’utente ha del titolare del trattamento e delle sue pratiche, in particolare in materia di privacy, è generalmente inesistente. Il titolare del trattamento nel suo insieme ha una comprensione molto più chiara delle sue politiche. Inoltre inizia a sapere molto sull'utente in un breve periodo di tempo, se non già ben informato. L'utente deve compiere sforzi sufficienti per indagare sul titolare del trattamento per conoscere le sue pratiche per fornire un consenso valido. Il titolare del trattamento necessita di questo valido consenso per trattare lecitamente le informazioni dell'utente.</p>
