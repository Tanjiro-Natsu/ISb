<h1 style="font-family:Impact;">Strategie</h1>
<br>
<p>La minimizzazione dei dati personali può essere ottenuta raccogliendo dati da
meno persone o raccogliendo meno dati sulle persone. Esistono diverse tattiche
applicabile.</p>
<br>
<h2 style="text-align:left;font-family:Impact;">-Isolare</h2>
<p>Raccogliere ed elaborare dati personali in diversi database o 
applicazioni. Questi database o applicazioni sono logicamente separati
,o effettivamente gestiti su diversi (ma comunque posizionati centralmente o
controllati) hardware. Assicurarsi che la separazione logica sia strattamente
forzata.</p>
<br>

<h2 style="text-align:left;font-family:Impact;">-Distribuire</h2>
<p>Distribuire la raccolta e il trattamento dei dati personali
in diverse posizioni fisiche utilizzando database e sistemi che 
non sono sotto il controllo di una singola entità. Utilizzare l'attrezzatura (PC,
laptop, smartphone) dell'interessato stesso, per quanto possibile
bile e utilizzare il meno possibile i componenti centrali. Usa 
architetture di sistema decentralizzate o addirittura distribuite anziché quelle centralizzate.</p>
<br>
<br>
<br>
<h1 style="font-family:Impact;">Esempi</h1>
<br>
<br>
<p>Un social network consente alle persone di condividere aggiornamenti di stato e immagini ad
amici e conoscenti. Gli attuali social network (come Twitter o
Facebook) sono architetture centralizzate: la piattaforma vede tutto ciò che 
i suoi utenti condividono tra loro. Ciò determina anche il (tremendo)
valore di questi social network, ed è anche sottolinea  il modello di 
queste business network. Una versione privacy-friendly di tale social network
consentirebbe ai suoi utenti di archiviare tutti gli aggiornamenti e le immagini localmente sul proprio
 dispositivo (ad esempio i loro smartphone) e condividendo tali informazioni
direttamente con i propri amici e conoscenti in modalità peer-to-peer.
In questo caso nessun dato viene memorizzato o elaborato da un componente centralizzato .
In generale si potrebbero utilizzare reti peer-to-peer o algoritmi distribuiti
invece di approcci centralizzati, per migliorare la tutela della privacy.</p>
