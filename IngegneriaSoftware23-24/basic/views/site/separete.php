    <p>Separare quanto più possibile il trattamento dei dati personali.
Un'altra strategia importante è separare (logicamente o fisicamente) i processi del
trattamento dei dati personali. Ciò rende più difficile combinare o correlare
dati. Trattando dati personali provenienti da contesti diversi, il rischio che
i dati di un contesto diventano noti in un altro contesto vengono ridotti. E
soprattutto quando la separazione è fisica. Quindi  separazione implementa l'
integrità contestuale

</p>

