<h3 style="text-align:left;font-family:Impact;margin:15px;" >Descrizione</h3>
<br>
<p style="text-align:left;margin:15px;">I gestori dei servizi (o prodotti) forniti agli utenti raccolgono enormi quantità di dati, in gran parte personali, per migliorare la qualità e l'esperienza dell'utente di tale servizio. Tutto ciò deve avvenire con il consenso informato dell’utente, che deve comprendere correttamente i rischi associati ai propri dati. Uno di questi rischi è quello dell’accesso, della modifica, della rimozione o della condivisione non autorizzata dei dati. Se si verifica una violazione dei dati, è necessaria la notifica. Qualsiasi titolare del trattamento dei dati all'interno dell'UE (o che fornisce servizi o prodotti all'interno dell'UE) deve informare l'autorità di controllo del suo stabilimento principale o rappresentante. La notifica deve essere effettuata entro 72 ore, salvo giustificazione. La notifica agli utenti dipende dal fatto che siano sufficientemente preoccupati.</p>

