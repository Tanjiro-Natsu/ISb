<h3 style="text-align:left;font-family:Impact;margin:15px;">Contesto</h3>
<br>
<p style="text-align:left;margin:15px;">I gestori dei servizi (o prodotti) forniti agli utenti raccolgono grandi quantità di dati, molti dei quali personali, per migliorare la qualità e l'esperienza dell'utente di tale servizio. Tutto ciò deve essere fatto con il consenso informato dell’utente, che dovrebbe comprendere adeguatamente i rischi associati ai propri dati. Uno di questi rischi è quello dell’accesso, della modifica, della rimozione o della condivisione non autorizzata dei dati. Se si verifica una tale violazione dei dati, è necessario avvisarci. Qualsiasi operatore all'interno dell'UE (o che fornisce servizi o prodotti all'interno) deve informare l'autorità di vigilanza del suo stabilimento principale o rappresentante. Ciò deve essere fatto entro 72 ore, salvo giustificazione. La notifica agli utenti dipende dal grado di impatto sufficiente sulla loro situazione.</p>
