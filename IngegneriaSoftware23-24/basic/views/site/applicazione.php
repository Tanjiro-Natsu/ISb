                <p>Tradizionalmente, lo sviluppo e l'implementazione del sistema sono un processo ciclico.
Il ciclo di vita di questo sistema procede attraverso diverse fasi: ideazione, definizione
, progettazione, sviluppo, dispiegamento, funzionamento, valutazione e decommissioning.
Le strategie di progettazione della privacy sono state 
sviluppate perché gli strumenti esistenti (modelli di progettazione e strumenti di miglioramento della privacy)
si applicano principalmente alla fase di progettazione e sviluppo. Mentre
durante le prime due fasi (ideazione e definizione) sono prese decisioni importanti
che hanno un impatto significativo sulle proprietà complessive della privacy
del sistema in fase di sviluppo.</p>
