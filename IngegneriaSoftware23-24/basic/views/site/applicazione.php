<p>Il fatto che le strategie di progettazione della privacy siano state sviluppate nel contesto
 della classica metodologia di sviluppo a cascata non significa
che non possono essere applicati in altri approcci più moderni come Agile. Anche in questi approcci vengono formulati concetti
e definiti.Solo che queste fasi sono incorporate in modo diverso in queste
approcci moderni.</p>
<br>
<p>Le strategie di progettazione della privacy impongono determinati obiettivi (tecnici) che, quando
raggiunti, migliorarano la privacy dell’intero sistema. Si potrebbe anche vedere
sotto forma di domande che è possibile porre durante il processo di sviluppo del software.
Come posso separare il trattamento dei dati personali? Come posso correttamente
informare i miei utenti sul trattamento dei loro dati personali?</p>
<br>
<p>Questo può essere fatto in modo strutturato. Assicurarsi che tutte le parti interessate coinvolti nel progetto
siano rappresentate , compreso il titolare del processo e
il perito tecnico. Assicurarsi inoltre che gli utenti finali (il cui informazioni personali
 verranno elaborate) siano rappresentati. Questo garantisce
che il processo di progettazione (e quindi l’analisi dei rischi)
tenga conto del punto di vista del titolare del trattamento, ma anche di quello degli
interessati</p>
<br>
<p>Le strategie di progettazione della privacy non sono rilevanti solo quando si sviluppi sistemi
tu stesso. Possono essere utilizzati anche negli appalti durante la determinazione
delle specifiche di un prodotto da acquistare altrove.
Non concentrarti su una sola strategia. Non è una decisione alternativa. Sono
tutte potenzialmente utili. Quindi applicali tutti, uno per uno, per creare il tuo sistema
il più rispettoso possibile della privacy. A seconda del contesto, però, tu
potresti trovare alcune strategie più applicabili di altre.</p>
<br>
<p>Considerare tutti gli aspetti legati al trattamento dei dati personali. Questo 
vuol  dire, considerare di operare, archiviare, conservare, raccogliere, condividere,
modificare e cancellare dei dati personali. Indagare per ciascuna di queste
strategie (e le tattiche associate) se si applicano a ciascuno di questi
aspetti.

</p>
<br>
<p>Non limitare la tua attenzione ai dati personali ordinari, ma considera anche
metadati (che potrebbero essere raccolti inavvertitamente).

</p>
<br>
<p>Infine, considera l'applicazione delle strategie di progettazione ai componenti del
sistema(invece del sistema nel suo insieme). In effetti si possono applicare
le strategie di progettazione della privacy in modo iterativo: un progetto iniziale basato su una prima
analisi utilizzando le strategie di progettazione della privacy può essere perfezionata applicando
le strategie nuovamente a un livello di dettaglio inferiore</p>
