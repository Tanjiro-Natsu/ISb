    <p>Limitare il più possibile il trattamento dei dati personali.
La strategia più ovvia per proteggere la privacy è ridurre al minimo la raccolta
 dei dati personali. Niente può andare storto con i dati che non raccogli:
non possono essere abusati, non possono essere utilizzati in modo improprio o fuoriuscire accidentalmente.
Pensa attentamente ai dati personali di cui hai veramente bisogno.
Un approccio radicalmente diverso può portare a una progettazione che richiede notevoli risorse.</p>

<br>
<br>
<br>
<h1 style="font-family:Impact;">Strategie</h1>
<br>
<p>La minimizzazione dei dati personali può essere ottenuta raccogliendo dati da
meno persone o raccogliendo meno dati sulle persone. Esistono diverse tattiche
applicabile.</p>
<br>
<h2 style="text-align:left;font-family:Impact;">-Selezionare</h2>
<br>
<p>Seleziona solo le persone rilevanti e gli attributi rilevanti. Determinare 
in anticipo quali persone e quali attributi sono rilevanti ed 
elaborare solo quei dati. Elabora solo i dati in entrata che soddisfano i 
criteri di selezione. Sii prudente quando stabilisci i
criteri: selezionare solo ciò che è strettamente necessario. Utilizza una white-list.</p>
<br>

<h2 style="text-align:left;font-family:Impact;">-Escludere</h2>
<br>
<p>Escludere in anticipo persone o attributi. Determinare in anticipo
quali persone o attributi sono irrilevanti. Non elaborare tali dati
oppure scartarli immediatamente se si riceveno. Sii liberale
nei motivi di esclusione: escludere il più possibile, a meno che tu
sei certo e puoi giustificare che ne hai bisogno. Utilizza una lista nera.</p>
<br>
<h2 style="text-align:left;font-family:Impact;">-Strip</h2>
<br>
<p>Rimuovere i dati (parziali) non appena non sono più necessari.Determinare in anticipo il momento in cui ti occorre un particolare dato e 
assicurati che verrà eliminato automaticamente non appena non è piu utile. Se
l'elemento dati fa parte di un record dati più grande, aggiornare il campo in un
valore predefinito che indica che non è specificato. Cambiamenti nell'organizzazione,
processi o servizi possono rendere alcuni dati irrilevanti prima della loro scadenza.</p>
<br>
<h2 style="text-align:left;font-family:Impact;">-Distruggere</h2>
<br>
<p>Rimuovere completamente i dati personali non appena non sono più
pertinente. Assicurarsi che i dati non possano essere recuperati. Non fare affidamento su strategie di rimozione logiche, che bloccano solo
l'accesso ai dati, ma lasciano tracce dei dati ancora accessibili
ad abili aggressori. Rimuovi i dati anche dai backup e utilizza
modi sicuri per cancellare i dati dai dischi rigidi.</p>
<br>
<br>
<br>
<br>
<h5>Considerare la minimizzazione ti costringe a riflettere attentamente sui valori fondamentali
della tua organizzazione e del core business in cui operi.Se non sei nel business principale di
profilazione i tuoi clienti per scopi pubblicitari, dovresti restare
lontano da quello. La minimizzazione ti costringe a essere specifico riguardo al tuo obiettivo
; non essere tentato di esprimerli in modo ampio.
</h5>
