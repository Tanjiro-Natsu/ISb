<p>In altre parole, anche quando si utilizzano, condividono, analizzano e arricchiscono i dati
è necessario considerare i modi per ridurre al minimo la serie finale di dati personali
mantenuto.
La differenza tra lo stripping e la distruzione dei dati è sottile: stripping
avviene a livello applicativo, mentre la distruzione si concentra sul 
livello fisico di archiviazione.
Esistono metodi collaudati per cancellare e distruggere veramente i dati dai 
dischi rigidi (ad esempio scrivendo dati casuali più volte su ciascuno
dei settori coinvolti). Un metodo efficiente per distruggere i backup è
crittografando i dati prima di eseguirne il backup. Associando determinati tasti
con particolari periodi di conservazione, tutti i dati per un particolare dato possono essere
distrutti semplicemente distruggendo la chiave associata.
Un’architettura di sistema radicalmente diversa può risultare in un ambiente molto più privato. Consideriamo ad esempio un sistema per il pagamento delle tasse
di strade a pedaggio. Un approccio potrebbe essere quello di identificare in modo univoco ogni volta che passa un'
auto ad ogni ingresso e uscita della strada a pedaggio. Ciò fornirebbe all'
operatore  informazioni dettagliate sulle abitudini di guida di tutti: un'
enorme violazione della privacy. Un altro approccio sarebbe quello di installare una piccola scatola
in ogni vettura che detrae automaticamente la tariffa richiesta ad un associato
account</p>
