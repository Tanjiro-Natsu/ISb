<h1 style="font-family:Impact;">Esempi</h1>

<br>
<br>
<p>L’esclusione o la selezione dei dati non è rilevante solo quando si raccolgono informazioni o quando i dati personali vengono ottenuti in altro modo, ma anche quando
utilizziamo i dati già raccolti. Garantire che i processi interni e le applicazioni
utilizzino solo i dati personali che sono veramente rilevanti. Assicurarsi che
solo i dati rilevanti vengono condivisi con terze parti. E fai attenzione quando il
trattamento dei dati personali crea nuovi dati personali: seleziona o escludi
anche in questo caso i nuovi dati che sono veramente (non) necessari.</p>
