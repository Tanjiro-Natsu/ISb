 <br><br><br>
    <p>Dimostrare che stai trattando i dati personali in modo privacy-friendly.
Questa strategia risponde ai nuovi requisiti di cui hanno bisogno le organizzazioni:
dimostrare il rispetto delle normative sulla privacy. La strategia dimostrativa
eè orientata all’esterno, verso le autorità di protezione dei dati (possibilmente
tramite il responsabile interno della protezione dei dati).</p>

<br>
<br>
<br>
<h1 style="font-family:Impact;">Strategie</h1>
<br>
<p>Le seguenti tattiche aiutano le organizzazioni a dimostrare la conformità.</p>
<br>
<h2 style="text-align:left;font-family:Impact;">-Registrare</h2>
<br>
<p>Documentare tutti i passaggi (importanti) effettuati. Registrare le decisioni e motivarle. Raccogli i log di sistema (e rispondi alle anomalie)</p>
<br>

<h2 style="text-align:left;font-family:Impact;">-Segnalare</h2>
<br>
<p>Segnalare i risultati di tali controlli all'Autorità Garante per la Protezione dei Dati
(DPA), o conservarli per riferimento futuro. Consultare, quando possibile, il
DPA regolarmente.

</p>
<br>
<h2 style="text-align:left;font-family:Impact;">-Controllare</h2>
<br>
<p>Controlla regolarmente i registri, ma controlla anche i processi organizzativi
in generale e il modo in cui i dati personali vengono trattati all’interno
l'organizzazione.</p>
<br>
<br>
<br>
<br>
<br>
<h5>Documentare in modo ampio ma efficiente le modalità con cui l'organizzazione
protegge le informazioni personali. Fatelo in modo indagabile e chiarificatore-
ner. Assicurarsi che la documentazione corrisponda alla realtà. Ottenere
certificato.
</h5>
<br>


            <br>
<br>
<br>
<br>

<h1 style="font-family:Impact;">Esempi</h1>

<br>
<br>
<p>Esecuzione di una valutazione d'impatto sulla protezione dei dati (DPIA), e soprattutto
registrare i risultati e documentare adeguatamente le decisioni prese
basandosi su di esso, è un buon punto di partenza. Una DPIA deve essere eseguita quando dati
personali verranno trattati. A volte è sufficiente una DPIA limitata. Quando
ciò indica che potrebbero sorgere rischi significativi per la privacy, una DPIA a tutti gli effetti
deve essere eseguito. Una DPIA deve essere ripetuta di tanto in tanto in quanto 
le circostanze potrebbero essere cambiate.
Un altro approccio è ottenere la certificazione rispetto a un riconoscimento (internazionale)
standardizzato per il rispetto della privacy (come TRUSTe o EuroPriSe). Alternativamente,
 un punto di riferimento con le altre organizzazioni che operano all'interno dello
stesso settore (commerciale) potrebbe fornire un ulteriore sostegno alla privacy e alla 
maturità della vostra organizzazione.
Utilizzando metodi formali e ambienti di sviluppo strutturati quando
lo sviluppo di nuovi sistemi è un vantaggio.</p>
