<h3 style="text-align:left;font-family:Impact;margin:15px;">Contesto</h3>
<br>
<p style="text-align:left;margin:15px;">I servizi socialmente orientati su Internet consentono alla loro base di utenti, spesso diversificata, di condividere contenuti. Queste masse di utenti e contenuti condivisi sono anche sufficientemente varie da scoraggiare l’attenzione individuale. I titolari del trattamento preferiscono proteggersi da ulteriore complessità e investimenti in funzionalità che forniscono loro meno dati. Gli utenti, tuttavia, sentono il bisogno di impostazioni sulla privacy per distinguere la loro propensione al rischio personale da quella normale. Ognuno di loro ha le proprie idee sulla sensibilità delle proprie informazioni, il che rende difficile implementare controlli sufficienti.</p>
