<h3 style="text-align:left;font-family:Impact;margin:15px;">Esempi</h3>
<br>

<p style="text-align:left;margin:15px;">Un provider di identità rilascia pseudonimi per autenticare gli utenti presso fornitori di servizi di terze parti, che a loro volta possono verificare l'autenticità di questi pseudonimi presso il provider di identità, senza conoscere la reale identità dell'utente. Tuttavia, l'Identity Provider conosce tutti i servizi richiesti dagli utenti, il che rivela le informazioni personali all'Identity Provider e gli consente di profilare gli utenti.</p>

