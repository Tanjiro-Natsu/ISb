<h1 style="font-family:Impact;">Esempi</h1>

<br>
<br>
<p>In molti casi (come sconti speciali per senior o junior, o regole che
richiedeno uno per verificare se qualcuno è un adulto) conta solo l'età, e
non la data di nascita. In questi casi è sufficiente registrare l'
attribuire “maggiore di diciotto anni” o “anziano”.
I contatori intelligenti sono un esempio di un sistema che astrae entrambi nello spazio
così come nel tempo. Per la stabilità della rete elettrica informazioni dettagliate sul consumo energetico di una singola famiglia non sono pertinenti. È sufficiente
monitorare, in tempo reale, il consumo energetico di un'intera strada o di un isolato di case.
Per determinare la bolletta elettrica non è necessario registrare i consumi energetici
in tempo reale. È invece sufficiente inviare l'energia accumulata
diciamo tre mesi alla volta al fornitore di energia.</p>
<br>
<p>La crittografia omomorfa consente di eseguire calcoli su dati crittografati
 senza apprendere i dati reali (non crittografati). Questo permette ad
una parte di calcolare la somma di una serie di misurazioni crittografate. Un'
altra parte può quindi decrittografare questa somma. Allo stesso tempo nessuno impara
nulla sulle misurazioni individuali.</p>
<br>
<p>I servizi basati sulla posizione necessitano la posizione dell'utente per mostrare informazioni relative ad
informazioni importanti (come un ristorante vicino, ad esempio). Ma a seconda
nel servizio questa posizione non deve essere esatta. Coordinate GPS precise
 di solito non sono necessarie. A volte una posizione grossolana arrotondata
in  un chilometro quadrato è sufficiente.Tutti i dati rilevanti quell'area vengo mandati direttamente al dispositivo. Le informazioni più dettagliate vengono filtrate localmente.</p>
<br>
<p>Questo tipo di clustering o cloaking implementa il principio del k-anonimato.
Questo principio richiede che i dati siano disturbati o mascherati in questo modo
che potrebbe applicarsi ad almeno k persone diverse, anch'esse rappresentate
nel set di dati o stanno utilizzando il servizio. In altre parole: a parte te,
i dati potrebbero appartenere ad almeno k − 1 altre persone. In base alla posizione dei
servizi, k dipende dalla dimensione dell'area e dal numero medio di
gente lì</p>
