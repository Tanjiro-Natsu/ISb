    <p>Limitare il più possibile il dettaglio in cui sono contenuti i dati personali
sono processati.
Mentre “minimizzare” obbliga a decidere se elaborare o meno una particolare 
 dato personale, il termine “astratto” affronta la questione più sottile
del livello di dettaglio con cui trattare i dati personali. Minore è il dettagliato di
un dato personale tanto minore sarà il rischio per la privacy
</p>

<br>
<br>
<br>
<h1 style="font-family:Impact;">Strategie</h1>
<br>
<p>Il trattamento dei dati personali in modo meno dettagliato può essere effettuato sia presso data subject
leve così come all' attribute level. Si applicano le seguenti tre tattiche.</p>
<br>
<h2 style="text-align:left;font-family:Impact;">-Raggruppare</h2>
<p>Aggregare informazioni su un gruppo di persone invece di 
elaborare le informazioni personali per ciascuna persona nel gruppo separato. Compilare profili di gruppo con informazioni medie riguardanti
i membri del gruppo.</p>
<br>

<h2 style="text-align:left;font-family:Impact;">-Riassumere</h2>
<p>Riassumere gli attributi dettagliati in categorie più grossolane e generiche. Ad esempio, utilizza una categoria di età anziché una data di nascita, o una città di residenza invece di un indirizzo completo, quando possibile.
</p>
<br>
<h2 style="text-align:left;font-family:Impact;">-Perturbare</h2>
<p>Non elaborare il valore esatto di un dato personale. Invece
utilizzare un'approssimazione di quel valore o modificare il valore con alcuni
rumori casuale. Ad esempio, invece di riportare l' esatta
posizione di una persona, riporta la sua posizione all'interno di alcuni punti casuali
distanziati dalla posizione reale.</p>
<br>
<br>
<br>
<h5>Le informazioni dettagliate spesso diventano meno rilevanti nel tempo. Registri dettagliati
sono necessari per agire rapidamente in caso di hack o di interruzione, ma dopo
qualche tempo solo una panoramica aggregata del numero totale di utenti di
un servizio (magari con alcune informazioni su quali parti specifiche di
 servizio sia  stato utilizzato) è ancora rilevante. Sii consapevole di ciò e pulisci i registri
di conseguenza.

Tieni presente che anche i profili di gruppo rappresentano un rischio per la privacy quando gli individui possono 
essere facilmente classificati come appartenenti ad un particolare gruppo (come le persone con una
determinata condizione medica o con un determinato profilo di rischio finanziario).
</h5>
<br>
<br>
