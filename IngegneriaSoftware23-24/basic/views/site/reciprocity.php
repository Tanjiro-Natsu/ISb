<h3 style="text-align:left;font-family:Impact;margin:15px;">Contesto</h3>
<br>
<p style="text-align:left;margin:15px;">Nei servizi in cui gli utenti possono contribuire socialmente o in modo collaborativo, la partecipazione può costituire il fondamento del modello di business del servizio. In queste situazioni la qualità e la frequenza dei contenuti influiscono sul successo del servizio e quindi gli utenti hanno un grande impatto sulla sua sopravvivenza. Il fatto che un singolo utente contribuisca o meno gioca un ruolo nella redditività, il che mette il controllore nella posizione di incoraggiare o imporre una partecipazione paritaria. Gli utenti potrebbero tuttavia rispondere negativamente a tali idee, soprattutto se non vedono potenziali guadagni degni del loro impegno e rischi personali per la privacy.</p>
