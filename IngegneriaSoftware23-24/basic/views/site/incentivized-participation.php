<h3 style="text-align:left;font-family:Impact;margin:15px;">Principi Privacy by Design </h3>
<br>
<p style="text-align:left;margin:30px;">   -Rispetto per l'utente</p>
<br>            
<br>
<br>
<h3 style="text-align:left;font-family:Impact;margin:15px;">Contesto</h3>
<br>
<p style="text-align:left;margin:15px;">Un titolare del trattamento trae diversi valori dalla partecipazione dei suoi utenti (ovvero gli interessati). Quanto più questi utenti partecipano, fornendo esplicitamente il contesto e fornendo implicitamente metadati (ad esempio statistiche e telemetria), tanto migliore sarà il controllore sotto diversi aspetti. Nonostante questa relazione chiave, la condivisione eccessiva può violare notevolmente il diritto alla privacy dell'utente. Molti titolari del trattamento mirano quindi a rispettare questo diritto quando traggono vantaggio dalle interazioni degli utenti. Poiché il titolare del trattamento dovrebbe riconoscere la necessità di un consenso legittimo specifico, informato e liberamente prestato, gli utenti sono informati delle insidie ​​​​di un tale sistema. Man mano che vengono informati, magari attraverso una combinazione di Privacy Dashboard o Awareness Feed, gli utenti possono bilanciare i compromessi relativi alla privacy. Ciò riduce al minimo i rischi per la privacy assunti in base alle scelte personali informate dell'utente e protegge il responsabile del trattamento dal compromettere inavvertitamente la privacy dell'utente. Il titolare del trattamento, tuttavia, desidera comunque la partecipazione e può quindi fare ulteriori concessioni o disposizioni per contribuire a rendere il compromesso utile o inesistente. A tal fine, il titolare del trattamento può integrare la propria strategia con una scelta più granulare, ad esempio con la divulgazione selettiva e l'interruzione selettiva.</p>
