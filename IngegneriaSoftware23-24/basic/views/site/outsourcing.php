<h3 style="text-align:left;font-family:Impact;margin:15px;">Contesto</h3>
<br>
<p style="text-align:left;margin:15px;">I titolari del trattamento spesso non hanno i mezzi per trattare in modo fattibile o sufficiente i dati che supervisionano nella misura in cui desiderano. In questi casi cercano un processore esterno o una terza parte per gestire il processo. Ciò in genere è in conflitto con il consenso già ottenuto dai loro utenti (i loro interessati), poiché l'ulteriore trattamento da parte di terzi non è necessariamente compatibile con gli scopi concordati. In queste situazioni il titolare del trattamento non ha ottenuto legalmente il consenso per questo trattamento e sarà responsabile se lo effettua.</p>
